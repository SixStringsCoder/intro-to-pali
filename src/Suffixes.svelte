    </ul>
      
      <p>Other suffixes expressing or forming nouns:</p>
    <ul>
      <li><span class="left-list-span italic-w-ipg">kār + ana: kāraṇa –</span> 			
        <span class="right-list-span">deed, doing</span>
      </li>
      <li><span class="left-list-span italic-w-ipg">vāha + ana: vāhana –</span> 		
