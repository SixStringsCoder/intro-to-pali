  <h3 class="heading-2-ipg">Section Thirteen</h3>
  <h3 class="heading-3-ipg">Suffixes</h3>

  <p class="paragraph-1-ipg">
    It has been shown in the previous chapters that words in Pāli derive from a certain number of roots. From these roots words are developed by adding terminations, prefixes and suffixes. Terminations (suffixes) used for the conjugation of verbs and declension of nouns, pointing out gender, person, tenses, participles and cases have been introduced in the previous chapters. 
  </p>

  <p class="paragraph-1-ipg">
    Words can get developed directly from the root (primary derivates) or from those already developed words (secondary derivates). 
    Staying with the example of the root: <span class="italic-w-ipg">-gam-</span> primary derivates are <a href="#section13-note-52" class="notes-ipg" id="section13-note-52-rtn"><span class="italic-w-ipg">formed</span><sup class="sup-spacing-ipg">52</sup></a>:     
  </p>
  <ul class="list-cont" id="list-cont-1">
    <li>
      <span class="left-list-span italic-w-ipg">gam + a: gama –</span>
      <span class="right-list-span">1.) adj. 	going, able to go </span>
    </li>
    <li>
      <span class="left-list-span"></span>
      <span class="right-list-span no-2">2.) noun	the act of going, course</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">gam + ana: gamana –</span>
      <span class="right-list-span">1.) adj.		conducive to, leading to</span>
    </li>
    <li>
      <span class="left-list-span"></span>
      <span class="right-list-span no-2">2.) noun	movement, walk, journey</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">gam + i + ka: gamika –</span> 			
      <span class="right-list-span">1.) adj.		setting out for a journey </span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">gām + a: gāma –</span>
      <span class="right-list-span">1.) adj. 	belonging to the village; </span>low, inferior, common </li>
    <li>
      <span class="left-list-span"></span>
      <span class="right-list-span no-2">2.) noun	a village</span>
    </li>
  </ul>

  <p class="paragraph-1-ipg">If prefixes or further suffixes are added to these primary derivates so called secondary derivates develop:</p>
  <ul class="list-cont" id="list-cont-2">
    <li>
      <span class="left-list-span italic-w-ipg">viha + ṃ + gama: vihaṅgama  –</span> 	 	
      <span class="right-list-span">going in the air</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">dur + anu + gama: duranugama  –</span> 		
      <span class="right-list-span">difficult to be followed</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">dūra + ṃ + gama: dūraṅgama  –</span> 		
      <span class="right-list-span">going a long way, far reaching</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">dūra + ṃ + gami: dūraṅgami –</span>		
      <span class="right-list-span">travelling, going far</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">saṃ + gama: saṅgama –</span>			
      <span class="right-list-span">going together, assembly</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">gāma + anta: gāmanta –</span>			
      <span class="right-list-span">the neighbourhood of a village</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">gāma + aka: gāmaka –</span>			
      <span class="right-list-span">a little village</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">an + ā + gāmi + tā: anāgāmitā –</span>		
      <span class="right-list-span">the fact of not returning</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">gamana + bhāva: gamanabhāva –</span>		
      <span class="right-list-span">the state of going away</span>
    </li>
    <li>
      <span class="left-list-span italic-w-ipg">gamana + ā + gamana: gamanāgamana –</span>	
      <span class="right-list-span">going & coming, rising & setting</span>
    </li>
  </ul>

