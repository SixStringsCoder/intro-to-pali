      </li>
    </ul>

    <p>Suffixes expressing or forming agents:</p>
    <ul>
      <li><span class="left-list-span italic-w-ipg">para + gū: paragū –</span> 			
        <span class="right-list-span">going to the other shore</span>
      </li>
      <li><span class="left-list-span italic-w-ipg">kāla + jñū: kālaññū –</span> 			
