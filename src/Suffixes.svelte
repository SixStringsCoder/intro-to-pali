      </li>
    </ul>

    <p>Suffixes expressing abstractness:</p>
    <ul>
      <li><span class="left-list-span italic-w-ipg">eka + tta: ekatta –</span> 			
        <span class="right-list-span">oneness</span>
      </li>
      <li><span class="left-list-span italic-w-ipg">kata + tta: katatta –</span> 			
