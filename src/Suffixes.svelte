      </li>
      <li><span class="left-list-span italic-w-ipg">vās + ant: vāsant –</span> 			
        <span class="right-list-span">dwelling</span>
      </li>
      <li><span class="left-list-span italic-w-ipg">cet + anā: cetanā –</span> 			
        <span class="right-list-span">thought</span>
      </li>
      <li><span class="left-list-span italic-w-ipg">sat + ya: sacca –</span> 			
        <span class="right-list-span">truth</span>
      </li>
      <li><span class="left-list-span italic-w-ipg">vid + yā: vijjā –</span> 			
