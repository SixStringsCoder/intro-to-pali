  <h3 class="heading-2-ipg">Section Nine</h3>
  <h3 class="heading-3-ipg">Adjectives and adverbs</h3>


  <p class="paragraph-1-ipg">
    Adjectives are declined according to the nouns they define. They can be grouped according to their suffixes which are similar to the nouns: <span class="italic-w-ipg">-a; -i; -u; -in; -ant; -mant; -vant</span>; taking all the three genders as the respective nouns do. Adverbs relate to the respective verbs and adjust their meaning and application in the context of the sentence. They belong to the group of indeclinables and may be pure adverbs or derivative adverbs.
  </p>

  <h4 id="section-9-1" class="subheading-1-ipg">1. Declination of adjectives</h4>
  <p class="paragraph-1-ipg">
    Although a few of the declination-endings of nouns are of rare occurrence, in general adjectives are declined in the same way as the nouns they describe. So the same adjective will be declined in the same case declinations with the respective endings in all the three genders and both numbers. 
  </p>

