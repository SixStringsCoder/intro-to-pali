  <h3 class="heading-2-ipg">Section Ten</h3>
  <h3 class="heading-3-ipg">Pronouns (<span class="italic-w-ipg">nāma</span>)</h3>

<p class="paragraph-1-ipg">In Pāli the pronouns present their individual declension. Especially the declension of the personal pronouns can hardly be compared with any of the declensions of the nouns, but the declension of demonstrative pronoun, relative pronoun and interrogative pronoun follow a similar pattern. </p>

<p class="paragraph-1-ipg">
  The demonstrative pronouns are: <span class="italic-w-ipg">so/sā/taṃ</span>, <span class="italic-w-ipg">eso/esā/etaṃ</span> and or <span class="italic-w-ipg">ena/naṃ</span>; all follow the same declension. <br>
  <span class="italic-w-ipg">Ayaṃ/idaṃ</span> refer to a person or thing in close vicinity, <span class="italic-w-ipg">asu/aduṃ</span> point to a person or thing further away, they follow the declensions in the same manner than those listed below.<br>
  Pāli further uses possessive pronouns: <span class="italic-w-ipg">mādīya; tādīya</span>, but they are of rare occurrence and are substituted by the genitive of the personal pronoun for the 1st and 2nd person; the 3rd person uses the genitive of the demonstrative pronoun.
</p>

<p class="paragraph-1-ipg">The indefinite pronouns formed with particles <span class="italic-w-ipg">ci/cid</span> and <span class="italic-w-ipg">api/pi</span> are often used and are listed below.</p>

