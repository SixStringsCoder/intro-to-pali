  <h4 id="section-10-11" class="subheading-1-ipg">11. The indefinite particle <span class="italic-w-ipg">ci</span></h4>

  <p class="paragraph-1-ipg">
    The indefinite particles <span class="italic-w-ipg">ci/cid</span> (-d- inserted before vocals) or <span class="italic-w-ipg">api/pi</span> are appended to and declined as the case forms of the interrogative pronoun, expressing ideas such as anyone, whichever, whoever, e.g.<br>
    Examples are:
  </p>

  <dl>
    <dt>Masc.</dt>
    <dd><span class="italic-w-ipg">koci puriso</span> – some man; </dd>
    <dd><span class="italic-w-ipg">kenaci purisena</span> – by some man;</dd>
    <dd><span class="italic-w-ipg">kassaci purissa</span> – of some man; etc.</dd>
    <dt>Neut.</dt>
    <dd><span class="italic-w-ipg">kiñci phalaṃ</span> – some fruit; </dd>
    <dd><span class="italic-w-ipg">kenaci phalena</span> – by some fruit;</dd>
    <dd><span class="italic-w-ipg">kassaci phalassa</span> – of some fruit; etc.</dd>
    <dt>Fem.</dt>
    <dd><span class="italic-w-ipg">kāci itthi</span> – some woman; </dd>
    <dd><span class="italic-w-ipg">kāyaci itthiyā</span> – by some woman;</dd>
    <dd><span class="italic-w-ipg">kassāci itthiyā</span> – of some woman; etc</dd>
  </dl>

