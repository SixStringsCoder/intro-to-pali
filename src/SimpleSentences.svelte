  <h3 class="heading-2-ipg">Section Five</h3>
  <h3 class="heading-3-ipg">Syntax: Examples of development of simple sentences</h3>
  <p class="paragraph-1-ipg">
    In Pāli the development of sentences follows in general the same syntax as in English, but the predicate always stands last. The object is preceded by the subject and followed by the predicate. Qualifying adverbs or adjectives further precede the verb, subject or object respectively. Time phrases or adverbs open the sentences whenever used. Pāli uses only indefinite articles; at times verbs (predicates) are not mentioned, but should be added in translations. Substantives, pronouns and adjectives often get combined, they agree in gender, number and case. 
  <p class="paragraph-1-ipg">   
    Experience of reading and working with the original texts will show that the apparently ambiguous complexity of the Pāli grammar becomes clear sooner rather than later and the development of understanding the meaning of sentences usually improves <a href="#section5-note-26" class="notes-ipg" id="section5-note-26-rtn">rapidly</a><sup class="sup-spacing-ipg">26</sup>. The preceding surveys of declension and conjunction illustrate a simple example of the following sentence <a href="#section5-note-27" class="notes-ipg" id="section5-note-27-rtn">construction</a><sup class="sup-spacing-ipg">27</sup>.     
  </p>

