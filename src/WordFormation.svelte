<section id="section-4" class="section-divider-ipg">
	<h3 class="heading-2-ipg">Section Four</h3>
	<h3 class="heading-3-ipg">Word-formation</h3>
	<p class="paragraph-1-ipg">
		<span class="italic-w-ipg"></span>
		It is worthwhile to take a short look at the development of words in <span class="italic-w-ipg">Pāli</span> in order to help individual study. If we understand the derivation of the words from their <a href="#section4-note-16" class="notes-ipg" id="section4-note-16-rtn">roots<sup class="sup-spacing-ipg">16</sup></a>, their development by addition of prefixes, suffixes, case-, gender-, and tense-terminations and inflections as well as certain changes within the root by substituting or altering the carrying vowel, it becomes easy to grasp this ancient language.
	</p>

	<p class="paragraph-1-ipg">
		Words are formed through the combination of syllables. According to the character of the syllable – which may be open if ending in a vowel, closed if ending in a consonant or with the<span class="italic-w-ipg">niggahīta</span>, light if ending in a short vowel and heavy if ending in a long vowel, <span class="italic-w-ipg">niggahīta</span>or consonant – and the period of time they resonate they undergo certain adjustments when combined.
	</p>

	<p class="paragraph-1-ipg">
		A long syllable is exactly equal to two short syllables. (The total length of a long syllable being constant, a double consonant tends to compress and shorten a long vowel preceding it, and itself gets shortened by the long vowel.) Double consonants are very frequent in <span class="italic-w-ipg">Pāli</span>and must be strictly pronounced as long consonants, thus: <span class="italic-w-ipg">-nn-</span>, as well as: <span class="italic-w-ipg">-ss-</span>, is like English -nn- and -ss- in `unnecessary`. Combinations of non-aspirate with aspirate consonants need a tender stop so the aspirates can get uttered slightly explosive: <span class="italic-w-ipg">pac-cud-dharati</span> / <span class="italic-w-ipg">ug-gac-chati</span>.
	</p>

	<p class="paragraph-1-ipg">
		These modifications in order to keep the flow of harmonious sound without disrupting the movement of the organs of the mouth led to various changes that are called <a href="#section4-note-17" class="notes-ipg" id="section4-note-17-rtn"><span class="italic-w-ipg">sandhi</span><sup class="sup-spacing-ipg">17</sup></a><span class="italic-w-ipg">sandhi</span>. In general they <a href="#section4-note-18" class="notes-ipg" id="section4-note-18-rtn">undergo<sup class="sup-spacing-ipg">18</sup></a>:
	</p>

	<ol class="para-list-ipg">
		<li>
			Contraction: (<span class="italic-w-ipg">bahu-upakaro: bahūpakaro</span> – great help;
			<span class="italic-w-ipg">mahā-odha: mahodha</span> – great flood)
		</li>
		<li>
			Elision: (<span class="italic-w-ipg">mahā-iddhiko: mahiddhiko</span> – having great strength;
			<span class="italic-w-ipg">brahma-loka-upago: brahmalokūpago</span> – reaching the brahmin worlds)
		</li>
		<li>
			Insertion: (<span class="italic-w-ipg">idha-āhu: idhamāhu</span> – thus said;
			<span class="italic-w-ipg">ajja-aggo: ajjataggo</span> – from this day onwards)
		</li>
		<li>
			Changes from sonant to semivowels: (<span class="italic-w-ipg">anu-ā-gacchati: anvāgacchati</span>
			– follow; <span class="italic-w-ipg">anu-eti: anveti</span> – follow)
		</li>
	</ol>

	<p class="paragraph-1-ipg">
		and further phonetic changes in case of consonants. Here the joining consonants undergo modifications by way of:
	</p>

	<ol class="para-list-ipg">
		<li>
			<a href="#section4-note-19" class="notes-ipg" id="section4-note-19-rtn">Assimilation<sup class="sup-spacing-ipg">19</sup></a>: creating similarity (<span class="italic-w-ipg">ud-gacchati: uggacchati</span>
			– rise; <span class="italic-w-ipg">iti-eva: icceva</span> – thus indeed, truly;
			<span class="italic-w-ipg">kud-kicca: kukkucca</span> – feeling of remorse)
		</li>
		<li>
			Adaption: a <span class="italic-w-ipg">niggahīta</span> changes into the respective group of the
			joining consonant (<span class="italic-w-ipg">taṇhaṃ-karo: taṇhaṅkaro</span> – with craving;
			<span class="italic-w-ipg">evaṃ-kho: evaṅkho</span> – thus then; <span class="italic-w-ipg">dhammaṃ-ca:
			dhammañca</span> – and the Dhamma)
		</li>
		<li>
			<a href="#section4-note-20" class="notes-ipg" id="section4-note-20-rtn">Metathesis<sup class="sup-spacing-ipg">20</sup></a>: two consonants undergo an interchange (<span class="italic-w-ipg">mahyaṃ: mayhaṃ</span>
			– me, mine; <span class="italic-w-ipg">kayīrati: karīyati</span> – done)
		</li>
		<li>
			Epenthesis: insertion of a vowel (<span class="italic-w-ipg">brahm-no: brahmuno</span> – of Brahma;
			<span class="italic-w-ipg">klesa: kilesa</span> – impurity)
		</li>
		<li>
			Aspiration: (<span class="italic-w-ipg">as-ti: atthi – is; es-ti: eṭṭhi</span> – wish, desire;
			<span class="italic-w-ipg">is-ta-: iṭṭha</span> – welcome, pleasing, agreeable)
		</li>
		<li>
			Simplification: reduction of consonants: (<span class="italic-w-ipg">kar-ssāmi: kas-s-sāmi:
			kassāmi: kāsāmi: kāhāmi</span> – I will do)
		</li>
	</ol>

