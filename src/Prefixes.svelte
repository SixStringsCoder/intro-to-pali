    <li>Sometimes these prefixes may get combined to adapt their meaning: <span class="italic-w-ipg">upa +saṃ: upasaṃ; 	saṃ + ud: samud; 	anu + pari: anupari</span>.</li>
    <li>Some adverbs also take the position of prefixes and are used likewise: <span class="italic-w-ipg">atthaṃ</span> – setting; <span class="italic-w-ipg">āvi</span> – visible; <span class="italic-w-ipg">pura</span> – in front.</li>
