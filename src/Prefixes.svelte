  <h3 class="heading-2-ipg">Section Twelve</h3>
  <h3 class="heading-3-ipg">Prefixes (<span class="italic-w-ipg">upasagga</span>)</h3>
  <p class="paragraph-1-ipg">
    <span class="italic-w-ipg"></span>
  </p>

  <p class="paragraph-1-ipg">
    The following survey presents some of the prefixes that are of common occurrence in Pāli. They are mostly added to verbs, but can get combined with nouns, derivates and adjectives as well. Prefixes do not always alter the meaning of the word they are attached to, but rather intensify, modify or adjust it. They may also express the opposite. Examples for usages of prefixes with the verb <span class="italic-w-ipg">gacchati</span> were introduced under section 4.1.
  </p>

