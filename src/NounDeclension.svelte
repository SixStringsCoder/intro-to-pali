  <h3 class="heading-2-ipg">Section Six</h3>
  <h3 class="heading-3-ipg">Declension of nouns (<span class="italic-w-ipg">nāma</span>)</h3>
  <p class="paragraph-1-ipg">
    In <span class="italic-w-ipg">Pāli</span> the nouns are grouped according to gender and ending. We find the nouns ending in: <span class="italic-w-ipg">a, ā, i, ī, u, ū</span> prevailing as well as certain rare endings with individual declensions like: <span class="italic-w-ipg">bhagavant, rajo, pitar,</span> etc.
  </p>

  <p class="paragraph-1-ipg">
  According to the three genders different inflections occur, but in general we find those inflections in common usages, that were presented in the declension of <a href="#section6-note-28" class="notes-ipg" id="section6-note-28-rtn"><span class="italic-w-ipg">gāma.</span><sup class="sup-spacing-ipg">28</sup></a>  The same applies to the eight cases, whose denotation remains somewhat the <a href="#section6-note-29" class="notes-ipg" id="section6-note-29-rtn">same<sup class="sup-spacing-ipg">29</sup></a> and whose usages can mainly be understood in the following connotation:
  </p>

  <!-- List -->
  <ol class="para-list-ipg">
    <li>
      <span class="italic-w-ipg">paṭhamā – </span>first or nominative case:  – expresses the agent of a sentence or the subject of the verb that is describing that action, both agree in number and gender.
    </li>
    <li>
      <span class="italic-w-ipg">dutiyā – </span>second or accusative case: – indicates the object of action, motion, the duration or space in which an action takes place, it is governed by transitive <a href="#section6-note-30" class="notes-ipg" id="section6-note-30-rtn">verbs<sup class="sup-spacing-ipg">30</sup></a>.
    </li>
    <li>
      <span class="italic-w-ipg">tatiyā –  </span>third or instrumental case: – indicates the agent, means or instrument by whom or which an action is performed.
    </li>
    <li>
      <span class="italic-w-ipg">catutthī – </span>fourth or dative case: – indicates the object to which an action is directed, or something is given. Some verbs govern the dative case.
    </li>
    <li>
      <span class="italic-w-ipg">pañcamī – </span>fifth or ablative case: – indicates the place, object or person from where an action, separation or direction originates, it also points out the reason or motive.
    </li>
    <li>
      <span class="italic-w-ipg">chaṭṭhī – </span>sixth or genitive case: – indicates possession, relationship, comparison and indirect object.
    </li>
    <li>
      <span class="italic-w-ipg">sattamī – </span>seventh or locative case: – indicates the place, time or reason where, when or why an action, takes place.
    </li>
    <li>
      <span class="italic-w-ipg">aṭṭhamī – </span>eighth or vocative case: – is used to address one or more persons.
    </li>
  </ol>

